#include <stdio.h>

int main(void) 
{
    int x = 6, y = 8;
    printf("x: %d, y: %d\n", x, y);

    x ^= y;
    y ^= x;
    x ^= y;
    printf("swapped, x: %d, y: %d\n", x, y);
}

// Swap 2 variables with XOR. 

